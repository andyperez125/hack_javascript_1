/**
 * mediante el loop for agregar los números 0,1,2,3,4,5
 * dentro del array result 
 * 
 * 
 * output => [0,1,2,3,4,5]
 */


let result = [];

for (let i = 0; i <= 5; i++) {
    result.push(i); // Agrega los números del 0 al 5 en el array result
}

// Exporta el array result
module.exports = result;